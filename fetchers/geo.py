# ── Build fast lookup (longest names first for greedy matching) ────────────────
# Use simple string search instead of 495 compiled regex patterns.
# Names are sorted longest-first so "South Korea" matches before "South".
_SORTED_NAMES = sorted(_LOCATIONS.keys(), key=len, reverse=True)

# Pre-build lowercase versions for case-insensitive matching
_LOWER_NAMES = [(name, name.lower(), _LOCATIONS[name]) for name in _SORTED_NAMES]

# Characters that act as word boundaries
_BOUNDARY = set(' \t\n\r,.:;!?"\'-—–()[]{}/<>|@#$%^&*+=~`')


def _is_boundary(ch: str) -> bool:
    return ch in _BOUNDARY


def geocode_headline(title: str, categories: list = None, source: str = None) -> tuple:
    """
    Extract location from headline text and optional categories.
    Returns (lat, lon) or (None, None).
    Uses fast case-insensitive substring search with word-boundary checks.
    """
    search_text = title
    if categories:
        search_text += " " + " ".join(categories)
    lower_text = search_text.lower()
    text_len = len(lower_text)

    for name, lower_name, coords in _LOWER_NAMES:
        pos = lower_text.find(lower_name)
        if pos == -1:
            continue
        # Check word boundaries
        name_len = len(lower_name)
        before_ok = (pos == 0) or _is_boundary(lower_text[pos - 1])
        after_pos = pos + name_len
        after_ok = (after_pos >= text_len) or _is_boundary(lower_text[after_pos])
        if before_ok and after_ok:
            return coords

    # Fallback: source agency home location
    if source and source in SOURCE_COORDS:
        return SOURCE_COORDS[source]

    return (None, None)
